package Strings_Arrays_Program;

public class Odd_Even {

	public static void main(String[] args) {
		for(int i=0; i<=100; i++) {
			if(i%2==0) {
				System.out.println();
				System.out.println( i+" :It is even number");
				
			}
			else if(i%2!=0) {
				System.out.println();
				System.out.println( i+" It is odd number");
			}
		}

	}

}
