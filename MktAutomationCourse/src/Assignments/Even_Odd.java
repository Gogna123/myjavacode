package Assignments;

public class Even_Odd {

	public static void main(String[] args) {
		
		for(int i=0; i<=100; i++) {
		
			if(i%2==0) {
				System.out.println(i +" "+"no. is even");
				System.out.println();
			}
			else {
				System.out.println(i +" "+"no. is odd");
				System.out.println();
			}
		}
	}

}
