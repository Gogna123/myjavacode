package Assignments;

public class Arithmatic_Program {
	
	static void add() {
		int a=10;
		int b=20;
		System.out.println(a+b);
	}
	static void sub() {
		int a=200;
		int b=100;
		System.out.println(a-b);
	}
	static void mul() {
		int a=2;
		int b=10;
		System.out.println(a*b);
	}
	static void div() {
		int a=1000;
		int b=20;
		System.out.println(a/b);
	}
	static void modulas() {
		int a=250;
		int b=540;
		System.out.println(a%b);
	}
	

	public static void main(String[] args) {
		add();
		sub();
		mul();
		div();
		modulas();
		
	}

}
